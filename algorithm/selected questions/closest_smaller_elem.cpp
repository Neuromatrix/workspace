inline vector <pair <int, int>> closest_smaller_elem(vector <int> data){
    stack <int> tmp;
    vector <pair <int, int>> ans(data.size());
    for (size_t i = 0; i < data.size(); i++){
        ans[i].first = data[i];
    }
    tmp.push(data[0]);
    ans[0].second = ans[0].first;
    for (size_t i = 1; i < data.size();i++){
        while (!tmp.empty()){
            if (tmp.top() < data[i]) break;
            tmp.pop();
        }
        if(!tmp.empty()) ans[i].second = tmp.top();
        tmp.push(ans[i].first);
    } 
    return ans;
}
