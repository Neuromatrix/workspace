inline void solve(){
    int n, m;
    cin >> n >>  m;
    vi dist(max(2*m+1,2*n),INF);
    dist[n] = 0;
    queue <int> q;
    q.push(n);
    while (!q.empty()){
        int cur = q.front();
        q.pop();
        if(cur==m){
            break;
        }
        if(2*cur<=dist.size()){
            if(dist[2*cur]==INF){
                dist[2*cur] = dist[cur] + 1;
                q.push(2*cur);
            }
        }
        if(cur-1>=0){
            if(dist[cur-1]==INF){
                dist[cur-1] = dist[cur] + 1;
                q.push(cur-1);
            }
        }
    }
    cout << dist[m] << nl;
    
}
